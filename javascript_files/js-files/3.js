// string indexing means to access the string data through index numbers

const myAddress = "Peshawar Pakistan";

console.log(myAddress[0])
console.log(myAddress[1])

// there are 2 ways of string indexing 
// forward indexing
console.log(myAddress[0])
console.log(myAddress[1])
console.log(myAddress[2])

// reverse indexing
console.log(myAddress[myAddress.length-1])
console.log(myAddress[myAddress.length-2])
console.log(myAddress[myAddress.length-3])
