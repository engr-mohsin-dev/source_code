// operators in JS
// Operators in JavaScript are symbols or keywords that perform operations on values and variables.

let number_1 = 10;
let number_2 = 90;

console.log(number_1 + number_2);               // addition
console.log(number_1 - number_2);               // substraction
console.log(number_1 * number_2);               // multipication
console.log(number_1 / number_2);               // division
console.log(number_1 % number_2);               // modolus
console.log(number_1 ** number_2);               // power
console.log(number_1 < number_2);               // lessthen
console.log(number_1 <= number_2);               // lesserthen or equal
console.log(number_1 > number_2);               // greaterthen
console.log(number_1 >= number_2);               // greaterthen or equal
console.log(number_1 == number_2);               // equal        (no data type check)
console.log(number_1 === number_2);               // equal        (data type check)
console.log(number_1 != number_2);               // not equal        (not data type check)
console.log(number_1 !== number_2);               // not equal        (data type check)



